# ═══════════════════════════════════════════════════════
#  单日日报 Prompt
# ═══════════════════════════════════════════════════════

def build_single_day_prompt(date_str: str, emails_markdown: str) -> str:
    """单日邮件日报 Prompt。

    Args:
        date_str: 日期字符串 "YYYY-MM-DD"
        emails_markdown: 邮件 Markdown 内容（已由 builder.py 从文件读入并拼接）
    """
    weekday = _weekday(date_str)
    return f"""日期：{date_str}（周{weekday}）

以下是当天所有邮件的 Markdown 归档：

{emails_markdown}

格式要求：
- 直接从 `# 邮件日报` 开始输出，前面不能有任何文字
- `## 邮件清单`、`## 今日要点` 标题必须原样使用
- 撤回邮件统一放最后，标注"〔已撤回〕"
- 输出以 `---` 分隔线和尾注结束

特殊情况：
- 当天 0 封邮件：统计填 0，"邮件清单"写"当日无邮件"，"今日要点"写"当日无待办事项"
- 超过 30 封邮件：同类通知/系统邮件可合并，优先展示人工发送的非群发邮件

输出格式：

# 邮件日报 · {date_str}（周{weekday}）
> 当日邮件摘要概览

| 项目 | 数值 |
|------|------|
| 收件总数 | N 封 |
| 有效邮件 | N 封 |
| 撤回邮件 | N 封 |
| 带附件邮件 | N 封 |
| 收件时间段 | HH:MM – HH:MM |

## 邮件清单

### [邮件标题]
- **时间**：HH:MM
- **发件人**：姓名（邮箱）
- **收件人**：姓名（邮箱）
- **附件**：N 个 — 文件名描述 / 无
- **摘要**：一句话中文概括核心内容
- **关键信息**：
  - 要点一
  - 要点二
- **行动**：需要做什么 / 无需行动

### [邮件标题]
（同上格式，每封邮件一个 ### 小节）

### 〔已撤回〕邮件标题
- **状态**：✗ 发件人已撤回，可忽略

## 今日要点
1. 核心发现一
2. 核心发现二

---
*本日报由 Email Agent 基于本地归档生成 · 数据来源：`output/{date_str}/`*"""


# ═══════════════════════════════════════════════════════
#  多日汇总 Prompt
# ═══════════════════════════════════════════════════════

def build_aggregate_prompt(
    start: str, end: str, date_list: list[str], daily_summaries: str
) -> str:
    """聚合多日日报为一个综合汇总报告。

    Args:
        start: 起始日期 "YYYY-MM-DD"
        end: 结束日期 "YYYY-MM-DD"
        date_list: 需要汇总的日期列表
        daily_summaries: 所有单日日报内容拼接（已由 builder.py 读入）
    """
    return f"""请汇总 {start} ~ {end}（共 {len(date_list)} 天）的邮件日报为一份综合报告。

以下是每日日报内容：

{daily_summaries}

格式要求：
- 直接从 `# 邮件汇总` 开始输出，前面不能有任何文字
- `## 待办清单`、`## 趋势概览` 标题必须原样使用
- 全局待办需去重合并，相似行动项归一处理
- 每日重点表每条一行，不复制完整日报内容

特殊情况：
- 某日邮件数为 0：封数列填 0，重点摘要写"当日无邮件"
- 某日日报缺失：日期表标注"数据缺失"，不影响其他日期

输出格式：

# 邮件汇总 · {start} ~ {end}
> {len(date_list)} 天 · 基于每日日报汇总

| 日期 | 封数 | 重点摘要 |
|------|------|----------|
| YYYY-MM-DD（周X） | N 封 | 当日核心内容一句话 |
| ... | ... | ... |

## 待办清单
- [ ] 跨天去重合并后的所有行动项

## 趋势概览
- **邮件量走势**：描述每天邮件量变化趋势
- **高频主题**：出现频率最高的邮件主题关键词
- **最活跃发件人**：发件频率最高的人/系统
- **需重点关注**：需要用户特别关注的事项

---
*本报告由 Email Agent 基于每日日报汇总生成 · {datetime.now().strftime('%Y-%m-%d %H:%M')}*"""


# ═══════════════════════════════════════════════════════
#  工具函数
# ═══════════════════════════════════════════════════════

def _weekday(date_str: str) -> str:
    try:
        return ["一", "二", "三", "四", "五", "六", "日"][
            datetime.strptime(date_str, "%Y-%m-%d").weekday()
        ]
    except Exception:
        return "?"
