def build_single_day_prompt(date_str: str) -> str:
    """单日邮件日报 Prompt。

    用于首次生成某一天的日报 —— 读原始 .md 邮件文件。
    """
    weekday = _weekday(date_str)
    return f"""读取 output/{date_str}/markdown/ 目录下的 .md 邮件文件，提取每封邮件的 YAML front matter 元数据（subject/from/date）和正文，按下方格式生成日报。仅读取这个目录，不触碰其他目录。如果邮件 front matter 的 date 字段不是 {date_str}，跳过该邮件。

日期：{date_str}（周{weekday}）

格式铁律（必须遵守）：
- 直接从 `# 邮件日报` 开始输出，前面不能有任何文字
- 日报以 `*本日报由 Email Agent 基于本地归档生成 · 数据来源：output/{date_str}/` 尾注结束
- `## 邮件清单`、`## 今日要点` 标题文字必须原样出现，不可自创
- 每封邮件下 `- **时间**` `- **发件人**` `- **摘要**` `- **行动**` 四个字段必须原样出现
- 撤回邮件单独放最后
- 中文输出，英文内容翻译为中文摘要；非中/英文邮件标注原语言并翻译摘要
- 禁止任何提问、确认、解释或交互语句
- 必须原样输出 `---` 分隔线和 `*本日报由 Email Agent...*` 尾注行，即使输出被截断也要优先保留这行

边界情况处理规则：
- 当天 0 封邮件：统计表全部填 0，"邮件清单"下写"当日无邮件"，"今日要点"写"当日无待办事项"
- 当天超过 30 封邮件：同类通知/系统邮件可合并为一行摘要，优先展示人工发送的非群发邮件
- 邮件正文为空（纯附件）：摘要标注"仅附件：文件名"，不编造正文内容
- 同一发件人多封相似通知邮件：合并为一条，摘要注明"共 N 封通知"，避免清单冗长
- 非中/英文邮件：摘要中标注原语言（如"〔日文〕…"或"〔韩文〕…"），并给出中文翻译

输出格式模板：

# 邮件日报 · {date_str}（周{weekday}）

| 项目 | 数值 |
|------|------|
| 收件总数 | N 封 |
| 有效邮件 | N 封 |
| 撤回邮件 | N 封 |
| 带附件邮件 | N 封 |
| 收件时间段 | HH:MM – HH:MM |

## 邮件清单

### [邮件标题]
- **时间**：HH:MM
- **发件人**：姓名（邮箱）
- **收件人**：姓名（邮箱）
- **附件**：N 个 — 文件名描述 / 无
- **摘要**：一句话中文概括核心内容
- **关键信息**：
  - 要点一
  - 要点二
- **行动**：需要做什么 / 无需行动

### [邮件标题]
（同上格式，每封邮件一个 ### 小节）

### 〔已撤回〕邮件标题
- **状态**：✗ 发件人已撤回，可忽略

## 今日要点
1. 核心发现一
2. 核心发现二

---
*本日报由 Email Agent 基于本地归档生成 · 数据来源：`output/{date_str}/`*"""


def build_aggregate_prompt(start: str, end: str, date_list: list[str]) -> str:
    """聚合多日日报为一个综合汇总报告。

    AI 通过 --add-dir 读取 output 目录下的各日 -summary.md 文件，
    避免命令行参数过长。
    """
    date_paths = "\n".join(
        f"  - output/{d}/{d}-summary.md" for d in sorted(date_list)
    )
    return f"""请汇总 {start} ~ {end}（共 {len(date_list)} 天）的邮件日报为一份综合周报/月报。

请读取以下日期的日报文件：
{date_paths}

只读取上述指定的文件，禁止遍历其他日期的目录。

格式铁律（必须遵守）：
- 直接从 `# 邮件汇总` 开始输出，前面不能有任何文字
- 报告以 `*本报告由 Email Agent 基于每日日报汇总生成 · ...*` 尾注结束
- `## 待办清单`、`## 趋势概览` 标题文字必须原样出现，不可自创
- 中文输出，专业简洁
- 每日重点表只保留最核心信息，每条一行，切勿复制当天完整日报内容
- 全局待办必须去重合并，相似行动项归一处理
- 禁止任何提问、确认、解释或交互语句
- 必须原样输出 `---` 分隔线和 `*本报告由 Email Agent...*` 尾注行，且必须是输出的最后一行

边界情况处理规则：
- 某日邮件数为 0：日期行正常列出，封数列填 0，重点摘要写"当日无邮件"
- 跨天出现相同主题/发件人的邮件：在"趋势概览"中汇总提及，待办清单中去重
- 某日日报文件不存在或读取失败：跳过该日，日期表中标注"数据缺失"，不影响其他日期处理

输出格式模板：

# 邮件汇总 · {start} ~ {end}
> {len(date_list)} 天 · 基于每日日报汇总

| 日期 | 封数 | 重点摘要 |
|------|------|----------|
| YYYY-MM-DD（周X） | N 封 | 当日核心内容一句话 |
| ... | ... | ... |

## 待办清单
- [ ] 跨天去重合并后的所有行动项

## 趋势概览
- **邮件量走势**：描述每天邮件量变化趋势
- **高频主题**：出现频率最高的邮件主题关键词
- **最活跃发件人**：发件频率最高的人/系统
- **需重点关注**：需要用户特别关注的事项

---
*本报告由 Email Agent 基于每日日报汇总生成 · {datetime.now().strftime('%Y-%m-%d %H:%M')}*"""


# ═══════════════════════════════════════════════════════
#  工具函数
# ═══════════════════════════════════════════════════════

def _weekday(date_str: str) -> str:
    try:
        return ["一", "二", "三", "四", "五", "六", "日"][
            datetime.strptime(date_str, "%Y-%m-%d").weekday()
        ]
    except Exception:
        return "?"
